    public void cargarDatos(String numAf2, String dni2){
        int numAf = Integer.parseInt(numAf2);
        Afiliado afil = emp.buscarAf(numAf);
        Familiar flia = emp.buscarFamiliar(afil, dni2);
        varDni.setText(flia.getDni());
        varNombre.setText(flia.getNombre());
        varApellido.setText(flia.getApellido());
        String edad = String.valueOf(flia.getEdad());
        varEdad.setText(edad);
        varSexo.setText(flia.getSexo());
        varDomicilio.setText(flia.getDomicilio());
        varTipoFamiliar.setText(flia.getTipoFamiliar());
        numAf1 = numAf;
        dni1 = dni2;
    }
